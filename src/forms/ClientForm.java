    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
         String nom=nomtxt.getText();
         String prenom=prenomtxt.getText();
         String telephone=telephonetxt.getText();
         String mail=emailtxt.getText();
         Client client=clientService.findById(id);
         client.setNom(nom);
         client.setPrenom(prenom);
         client.setTelephone(telephone);
         client.setEmail(mail);
         int answer=JOptionPane.showConfirmDialog(this, "Etes vous sur de modifier ce client?");
         if(answer==0){
             if(clientService.update(client)){
                 JOptionPane.showMessageDialog(this,"Client modifié");
                            load();
             }
         }
                 
    }//GEN-LAST:event_modifierActionPerformed

    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed
        // TODO add your handling code here:
        String nom = nomtxt.getText();
        String prenom = prenomtxt.getText();
        String telephone = telephonetxt.getText();
        String email = emailtxt.getText();
        if(clientService.create(new Client(nom, prenom, telephone, email))){
            this.load();
            nomtxt.setText("");
            prenomtxt.setText("");
            emailtxt.setText("");
            telephonetxt.setText("");
            JOptionPane.showMessageDialog(this, "Client bien ajouter");
        }else {
            JOptionPane.showMessageDialog(this, "Client non ajouter");
        }
            
        
    }//GEN-LAST:event_ajouterActionPerformed

