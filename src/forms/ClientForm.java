    private void listClientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listClientsMouseClicked
        id=parseInt(model.getValueAt(listClients.getSelectedRow(),0).toString());
        nomtxt.setText(model.getValueAt(listClients.getSelectedRow(),1).toString());
        prenomtxt.setText(model.getValueAt(listClients.getSelectedRow(),2).toString());
        telephonetxt.setText(model.getValueAt(listClients.getSelectedRow(),3).toString());
        emailtxt.setText(model.getValueAt(listClients.getSelectedRow(),4).toString());
    }//GEN-LAST:event_listClientsMouseClicked

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
           int answer=JOptionPane.showConfirmDialog(this, "Etes vous sur de supprimer ce client?");
        if(answer==0)
           if(clientService.delete(clientService.findById(id)))
           {
               JOptionPane.showMessageDialog(this,"Client supprimé");
                            load();

           }
    }//GEN-LAST:event_supprimerActionPerformed

    private void load(){
        model.setRowCount(0);
        for(Client c : clientService.findAll()){
            model.addRow(new Object[]{c.getId(),c.getNom(),c.getPrenom(),c.getTelephone(),c.getEmail()});
        }
    }
